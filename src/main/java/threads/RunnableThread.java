package threads;
//----- Imports ------------------------------------
//--------------------------------------------------

//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
//~~~~~

public abstract class RunnableThread implements Runnable
{

	//--------------------------------------------------
	//----- _ ------------------------------------------
	//--------------------------------------------------

	//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	//~~~~~

	//--------------------------------------------------
	//----- Constants ----------------------------------
	//--------------------------------------------------

	//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	//~~~~~
	//--------------------------------------------------
	//----- Variables ----------------------------------
	//--------------------------------------------------
	ThreadListener listener;
	//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	//~~~~~

	public RunnableThread(ThreadListener listener)
	{
		this.listener = listener;
	}

	public abstract void onRun();

	@Override
	public void run()
	{
		onRun();
		listener.finished();
		try
		{
			Thread.sleep(1000);
		}
		catch (Exception e)
		{

		}
	}

}
