import java.util.ArrayList;

public class DataTracker
{
	
	//--------------------------------------------------
	//----- _ ------------------------------------------
	//--------------------------------------------------
	
	//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	//~~~~~
	
	//--------------------------------------------------
	//----- Constants ----------------------------------
	//--------------------------------------------------
	
	//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	//~~~~~
	//--------------------------------------------------
	//----- Variables ----------------------------------
	//--------------------------------------------------
	private int numVars;
	
	ArrayList<ArrayList<Double>> errorVals;
	//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	//~~~~~
	
	public DataTracker(final int numVars)
	{
		this.numVars = numVars;
		
		errorVals = new ArrayList<>();
		for (int i = 0; i < numVars; i++)
		{
			errorVals.add(new ArrayList<>());
		}
	}
	
	public void addErros(final double[] errors)
	{
		for (int varNum = 0; varNum < numVars; varNum++)
		{
			errorVals.get(varNum).add(errors[varNum]);
		}
	}
	
}
